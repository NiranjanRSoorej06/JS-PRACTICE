var BASIC_URL="https://api.exchangerate.host/convert?";

var dropdowns=document.querySelectorAll("#country select");

var btn=document.querySelector("#btn");

var fromCurr=document.querySelector("#from #without-text select");
var toCurr=document.querySelector("#to #without-text select");

var result=document.querySelector(".msg");

for(var select of dropdowns){
    for(currcode in countryList){
        var newOption = document.createElement("option");
        newOption.innerText=currcode;
        newOption.value=currcode;

        if(select.name==="from" && currcode==="USD")
            {
                newOption.selected="USD";
            }

        if(select.name==="to" && currcode==="INR")
                {
                    newOption.selected="INR";
                }
        
        select.appendChild(newOption);
    }    

    select.addEventListener("change", (evt) => {
        updateFlag(evt.target);
    });
};

const updateFlag = (element) =>{
    var currencycode=element.value;
    var countrycode=countryList[currencycode];
    var newsrc="https://flagsapi.com/"+countrycode+"/flat/64.png";
    var image=element.parentElement.querySelector("img");
    image.src=newsrc;
};

btn.addEventListener("click",async (evt) => {
    evt.preventDefault();

    let amount=document.querySelector("#amount-box");
    let amtval=amount.value;

    if(amtval==="" || amtval<0){
        amtval=1;
        amount.value=1;
    }


    const URL = BASIC_URL+"from="+fromCurr.value.toUpperCase()+"&to="+toCurr.value.toUpperCase()+"&amount="+amtval+"&access_key=16745a91c44341b85cc2c76300d5f995";
    let response = await fetch(URL);
    let data= await response.json();
   result.innerText=amtval+" "+fromCurr.value.toUpperCase()+" = "+data.result+" "+toCurr.value.toUpperCase();

})
